def calc_all():
    """Calculate IE for all atoms using unified screening + v19 alpha."""
    results = []

    for Z, sym, E_obs, config in atoms:
        val_n = max(nn for nn, ll, c in config)
        s_count = sum(c for nn, ll, c in config if nn == val_n and ll == 0)
        p_count = sum(c for nn, ll, c in config if nn == val_n and ll == 1)
        dc_val = sum(c for nn, ll, c in config if nn == val_n - 1 and ll == 2)

        is_pd = (s_count == 0 and dc_val == 10)
        n = val_n - 1 if is_pd else val_n
        has_p = p_count > 0 and not is_pd

        val_l = max([ll for nn, ll, c in config if nn == val_n and c > 0], default=0)
        val_block = 'p' if has_p else ('d' if (dc_val > 0 or is_pd) else 's')

        # === UNIFIED SCREENING ===
        S_core = core_screening_unified(config, n, is_pd, val_l, val_block)
        Z_net = Z - S_core

        # === PENETRATION (same as v19) ===
        pen = gamma_pen * S_core / (d * n + (d + 1) * abs(S_core)) if S_core != 0 else 0
        has_d_any = any(c > 0 for nn, ll, c in config if ll == 2)
        if n >= n_ref and not has_d_any:
            pen *= 1 / d**2

        # === ALPHA (keep v19 structure for now) ===
        # The N_eff and parity calculations stay the same —
        # we're only replacing the SCREENING function

        sp = (s_count == 2)

        if not has_p:
            # s/d-block
            if sp:
                parity = 1
            elif s_count == 1 and dc_val > 0 and not is_pd:
                parity = w_pi + (n - n_ref)
            elif is_pd:
                parity = w_delta + J_hund
            else:
                parity = -1

            # TM corrections (simplified from v19)
            if sp and dc_val > 0 and not is_pd:
                dc = dc_val
                if dc <= 5:
                    t2g_paired = 0
                elif dc <= 8:
                    t2g_paired = dc - 5
                else:
                    t2g_paired = d
                t2g_full = (t2g_paired == d)

                if t2g_full:
                    eg_occ = dc - 5 if dc > 5 else 0
                    eg_paired = max(0, dc - 5 - (5 - d)) if dc > 8 else 0
                    eg_unp = eg_occ - eg_paired
                    parity -= eg_unp * abs(w_delta) / d

                is_d10 = (dc == 10)
                if is_d10:
                    parity += J_hund

                parity += w_pi * (n - n_ref)

                n_deep_d10 = sum(1 for nn2, ll2, c2 in config
                                if ll2 == 2 and c2 == 10 and nn2 < n - 1)
                if is_d10:
                    parity += J_hund * n_deep_d10

            alpha = (d * n + parity) / (d**2 * n) - pen

        else:
            # p-block
            pc = p_count
            pa = min(pc, 2*d - pc) if pc <= d else 2*d - pc
            pl = max(0, pc - d)

            N_eff = 0
            if pc <= d:
                # Underfill
                uf_boost = (1 + w_pi) / d**2
                has_d_ch = any(c > 0 for nn2, ll2, c in config if ll2 == 2)
                if has_d_ch:
                    uf_boost *= d
                N_eff = pa + uf_boost
                xu = comb(pa, 2) if pa >= 2 else 0
                N_eff -= xu / (d**2 * n)
            else:
                # Overfill
                fl = 1
                rl = pl - fl if pl > 1 else 0
                w_first = (1 + w_pi)
                w_rest = 1 + w_pi - d / n**2
                N_eff = pa + fl * w_first + rl * w_rest

                xo_pairs = comb(pl, 2) if pl >= 2 else 0
                N_eff -= xo_pairs / (d**2 * n_ref)

                xa = comb(pa, 2) + comb(pl, 2) if (pa >= 2 or pl >= 2) else (comb(pa,2) if pa >= 2 else 0)
                xp_over = xa - (comb(pa,2) if pa >= 2 else 0)
                N_eff -= xp_over * (n - n_ref) / (d * (d - 1) * n)

            # Mode-vacancy
            N_eff += pa * (d - pa) / d**3

            # Deep Hund scattering
            deep_hl_ch = sum(min(c, 2*ll+1) for nn2, ll, c in config
                           if ll >= 2 and c > 0 and (n - nn2) >= 2)
            xu_total = comb(min(pc, d), 2)
            if xu_total > 0 and deep_hl_ch > 0:
                N_eff -= xu_total * deep_hl_ch / (d**3 * n)

            alpha = (d + w_pi * N_eff - 1) / d**2 - w_pi * pen

        # === ENERGY ===
        Z_eff = Z_net ** alpha
        E_pred_base = (Z_eff / n)**2 * E_H

        # Lattice shear
        N_core = sum(c for nn, ll, c in config if nn < n)
        shear = 1 + gamma_pen * N_core / (d**2 * n**2)
        E_pred = E_pred_base * shear

        err = (E_pred - E_obs) / E_obs * 100
        results.append((Z, sym, E_obs, E_pred, err, alpha))

    return results


