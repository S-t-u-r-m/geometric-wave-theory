def mass_ratio(l1, l2):
    """Breather mass ratio: sin((l1+1)*g) / sin((l2+1)*g)

    In small-gamma limit: ≈ (l1+1)/(l2+1)
    """
    return np.sin((l1 + 1) * gamma_sg) / np.sin((l2 + 1) * gamma_sg)


def parity_sign(l_core, l_val):
    """Angular parity: same parity screens (+), different anti-screens (-)

    Even l: s(0), d(2)
    Odd l: p(1), f(3)
    """
    if (l_core % 2) == (l_val % 2):
        return +1  # same parity: screening
    else:
        return -1  # different parity: anti-screening


def unified_screening(l_core, l_val, delta_n=1):
    """Unified screening weight per channel.

    Combines:
    - Mass ratio (magnitude): sin((lc+1)*g) / sin((lv+1)*g)
    - Angular parity (sign): (-1)^(lc+lv)
    - Depth attenuation: d/(d + delta_n) for deep shells
    - Normalization: divide by (2*l_core+1) to get per-channel weight

    This single function replaces v19's:
    - Rule 2 (core screening weights)
    - Rule 8 (f-core d-shell rebalancing)
    - Rule 9 (valence-dependent f-screening)
    """
    sign = parity_sign(l_core, l_val)
    mag = mass_ratio(l_core, l_val)
    n_ch = 2 * l_core + 1

    # Per-channel weight: mass ratio normalized by channel count
    w = sign * mag / n_ch

    # Depth attenuation for deep shells (delta_n >= 2)
    if delta_n >= 2:
        w *= d / (d + delta_n)

    return w


def core_screening_unified(config, core_ref_n, is_pd, val_l, val_block):
    """Core screening using unified mass-ratio coupling."""
    S = 0.0
    for nn, ll, count in config:
        if nn < core_ref_n:
            n_ch = min(count, 2 * ll + 1)
            delta_n = core_ref_n - nn
            w = unified_screening(ll, val_l, delta_n)
            S += n_ch * w
        elif nn == core_ref_n and is_pd and ll != 2:
            w = unified_screening(ll, val_l, delta_n=1)
            S += min(count, 2 * ll + 1) * w
    return S


